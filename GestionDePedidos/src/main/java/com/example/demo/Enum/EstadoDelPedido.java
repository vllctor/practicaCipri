package com.example.demo.Enum;

/**
 * Representa los posibles estados de un pedido.
 */
public enum EstadoDelPedido {
    /**
     * Estado cuando el pedido ha sido creado.
     */
    CREADO,
    
    /**
     * Estado cuando el pedido ha sido procesado.
     */
    PROCESADO,
    
    /**
     * Estado cuando el pedido ha sido borrado.
     */
    BORRADO,
    
    /**
     * Estado cuando el pedido ha sido enviado.
     */
    ENVIADO
}
